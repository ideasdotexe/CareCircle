// Normalize a Supabase caregiver_profiles row into the same shape as CAREGIVER_DIRECTORY
export function normalizeSupabaseCaregiver(row) {
  const name = row.full_name || row.name || 'Caregiver';
  const initials = name.split(/\s+/).map(w => w[0]).filter(Boolean).join('').slice(0, 2).toUpperCase();
  return {
    id: row.id,
    name,
    initials,
    photoTone: ['#3F5D54', '#2E4942'], // default sage tone for real profiles
    title: row.title || '',
    yearsExp: row.years_exp || row.years_experience || 0,
    verified: row.verified || false,
    city: row.city || '',
    region: row.region || row.city || '',
    province: row.province || '',
    country: row.country || 'Canada',
    rate: row.rate ? (typeof row.rate === 'number' ? `$${row.rate}/hr` : row.rate) : '',
    rating: row.rating || 0,
    reviewCount: row.review_count || 0,
    languages: Array.isArray(row.languages) ? row.languages : (row.languages ? [row.languages] : []),
    specialties: Array.isArray(row.specialties) ? row.specialties : (row.specialties ? [row.specialties] : []),
    available: row.available || row.availability || '',
    bio: row.bio || '',
    reviews: Array.isArray(row.reviews) ? row.reviews : [],
    _isReal: true, // flag for send-request flow to know this is a real Supabase profile
  };
}
