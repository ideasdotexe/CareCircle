-- Auto-create a profiles row whenever a new user signs up (any auth method).
-- This ensures full_name and role are always available from the moment of signup.

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, full_name, role, last_login)
  values (
    new.id,
    coalesce(new.raw_user_meta_data->>'full_name', split_part(new.email, '@', 1)),
    coalesce(new.raw_user_meta_data->>'role', 'dear_one'),
    now()
  )
  on conflict (id) do nothing;
  return new;
end;
$$;

-- Drop if it already exists, then recreate
drop trigger if exists on_auth_user_created on auth.users;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();
